#include<stdio.h>
int main()
{
    // initial condition (starting point)
    // final condition (termination/ending point)
    // operation (++/--)

    
    int i=1;               // initial condition
    while (i<=10)          // final condition
    {
        printf("%d ",i);
        i++;               // operation
    }
    
    
    return 0;
}