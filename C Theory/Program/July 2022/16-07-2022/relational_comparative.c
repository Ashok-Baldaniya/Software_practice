#include <stdio.h>
int main()
{ // relational or comparative operator (<,>,<=,>=,==,!=) the Ans is always in 0 and 1.

    int a = 5;
    printf("%d\n", a < 5);
    printf("%d\n", a > 5);
    printf("%d\n", a <= 5);
    printf("%d\n", a >= 5);
    printf("%d\n", a != 5);
    printf("%d\n", a == 5);

    return 0;
}